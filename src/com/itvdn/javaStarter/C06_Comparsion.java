package com.itvdn.javaStarter;

//import static java.lang.Math.pow;

public class C06_Comparsion {

    public static void main (String[] args){
        byte value1 = 0, value2 = 1;
        boolean result;

        result = value1 < value2;
        System.out.println(result);

        result = value1 > value2;
        System.out.println(result);

        result = value1 >= value2;
        System.out.println(result);

        result = value1 <= value2;
        System.out.println(result);

        result = value1 == value2;
        System.out.println(result);

        result = value1 != value2;
        System.out.println(result);

    }

}